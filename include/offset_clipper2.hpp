#pragma once

// Stage B of plan-20260514-clipper2-offset-backend.md.
//
// Pure-geometry layer: Clipper2 InflatePaths wrapper that translates
// a PreVABS yz-plane polyline into a Clipper2 PathD, runs the offset,
// and projects each output vertex back onto a base segment so the
// downstream reverse-matching bridge (Stage C) can build a
// BaseOffsetMap.
//
// **No PDCELVertex / BaseOffsetMap awareness.** This module is
// deliberately decoupled from the rest of PreVABS so the same
// `offsetWithClipper2` can be reused by the long-term end-state
// nested-offset orchestrator (see plan-20260515 §8) without rewrite.
// Callers convert their PDCELVertex sequence to SPoint2 in yz before
// calling, and consume `OffsetPolygon` afterwards.

#include "geo_types.hpp"

#include <vector>

namespace prevabs {
namespace geo {

/// One offset output vertex's relationship to the input base curve.
///
/// `base_seg`:
///   - in [0, N_seg)  → this output vertex projects onto input segment
///                      `base_seg`, with foot of perpendicular at
///                      parameter `base_u ∈ [0, 1]` on that segment.
///   - -1             → no input segment lies within `1.5 * |dist|`
///                      of this output vertex. Such a vertex was
///                      generated by Clipper2 at a miter / bevel
///                      join point and has no input correspondence.
///
/// The caller (Stage C reverse-matching bridge) uses these annotations
/// to fold the output back onto the legacy `BaseOffsetMap` staircase.
struct OffsetVertexSource {
  int    base_seg;
  double base_u;
};

/// One polygon in the offset result. Clipper2 may return multiple in
/// disconnected-branch scenarios (e.g. thin TE airfoils where the
/// inset pinches the contour into separate pieces).
struct OffsetPolygon {
  std::vector<SPoint2>            points;
  std::vector<OffsetVertexSource> sources;   // size == points.size()
  // Clipper2's InflatePaths always returns closed paths regardless of
  // EndType (Butt / Square / Round merely cap the open ends; the
  // result is still a closed perimeter loop). The flag is kept for
  // forward-compatibility but is currently always true for non-empty
  // output.
  bool                            is_closed;
};

/// Pure-geometry Clipper2 offset wrapper.
///
/// @param base            Input polyline in the yz plane.
///                        For closed inputs, the caller may omit the
///                        trailing duplicate vertex; if it is present
///                        the wrapper drops it defensively.
/// @param base_is_closed  true  → EndType::Polygon (implicit closing
///                                 edge from back to front).
///                        false → EndType::Butt (open-ended).
/// @param side            +1 for outward, -1 for inward. Honored only
///                        for closed inputs; for open inputs Clipper2's
///                        InflatePaths requires positive delta and the
///                        result is always a Butt-capped surrounding
///                        polygon (no left/right distinction).
/// @param dist            Magnitude of offset distance, > 0.
///
/// Returns zero or more `OffsetPolygon` instances. Empty when
/// Clipper2 has eaten the input completely (typically caused by
/// local half-thickness < dist somewhere along the path).
///
/// Implementation choices (locked in by A0 evidence; see
/// local/issue-20260515-clipper2-airfoil-a0.md):
///   - JoinType::Miter, miter_limit = 2.0
///   - precision = 8 (the Clipper2 1.4.0 hard ceiling)
std::vector<OffsetPolygon> offsetWithClipper2(
    const std::vector<SPoint2>& base,
    bool                        base_is_closed,
    int                         side,
    double                      dist);

}  // namespace geo
}  // namespace prevabs
